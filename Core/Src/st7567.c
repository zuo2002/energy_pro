// 动画帧数
#define NUM_FRAMES 30

// 定义一个全局变量用于控制动画是否继续播放
bool animationPlaying = true;

// 函数功能：实现从中间向两边显示的动画效果，并可控制播放速度
void LDZ_ShowPictureAnimation(u8 page_start, u8 page_end, u8 col_start, u8 col_end, const uint8_t *picture_data) {
    u8 frame = 0;
    while (frame < NUM_FRAMES && animationPlaying) {
        // 计算当前帧在页和列方向上从中间向两边扩展的范围
        u8 center_page = (page_start + page_end) / 2;
        u8 center_col = (col_start + col_end) / 2;
        u8 page_range = (page_end - page_start) * frame / (NUM_FRAMES - 1);
        u8 col_range = (col_end - col_start) * frame / (NUM_FRAMES - 1);

        for (u8 page = page_start; page <= page_end; page++) {
            LDZ_SetAddr(page, col_start);
            for (u8 col = col_start; col <= col_end; col++) {
                if ((page >= center_page - page_range / 2 && page <= center_page + page_range / 2) &&
                    (col >= center_col - col_range / 2 && col <= center_col + col_range / 2)) {
                    LDZ_WriteDAT(picture_data[(page - page_start) * (col_end - col_start + 1) + (col - col_start)]);
                } else {
                    LDZ_WriteDAT(0);
                }
            }
        }

        frame++;
        // 使用HAL库的延时函数
        HAL_Delay(30); // 100毫秒延迟
    }
}